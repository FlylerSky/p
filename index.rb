ca khúc hay: ừ thì chia tay
[Verse 1]
Từ sông Hồng cuồn cuộn dâng,  
Đến biển xanh nơi cuối bờ,  
Nơi nắng sớm còn ngủ yên,  
Gió gọi về qua mái rơm.  

Bước chân xưa còn vững chãi,  
Dẫu bao gian khó chẳng ngại,  
Vượt đèo dốc, qua núi cao,  
Trái tim hừng hực khát khao.

[Chorus 1]
Ôi, Việt Nam, ngọn lửa thiêng soi sáng,  
Ngàn năm trôi, vẫn sáng lối tình nồng.  
Từ con tim, dân tộc vượt bão giông,  
Yêu hòa bình, ôm lấy từng giấc mơ.

[Verse 2]
Hồn sông núi hòa vào ta,  
Nghe từng câu ca chào đời,  
Dẫu qua mấy dòng sóng gió,  
Màu da, tiếng nói không rời.  

Cánh chim trời rợp biển xanh,  
Màu cờ thắm luôn ấm lành,  
Tự hào gửi lời từ xa,  
Cho con cháu thắm đậm đà.

[Chorus 2]
Ôi, Việt Nam, tựa ánh bình minh đỏ,  
Vượt qua bao mùa lá đổ, còn đây.  
Dẫu mưa bão, gió lạnh vây tháng ngày,  
Còn đọng lại, bóng dáng người vững tay.

[Bridge]
Dòng thời gian, tình yêu mãi không phai,  
Những năm tháng bên thềm vẫn vang xa,  
Dẫu đường đời còn lắm gian truân qua,  
Tình này, mãi thiết tha không tàn phai.  

[Verse 3]
Ngày hôm nay rực sáng lên,  
Xây niềm tin, xây hy vọng,  
Dòng máu đỏ còn chảy mãi,  
Khúc nhạc lòng reo mênh mông.  

Bao lớp người, nối tiếp nhau,  
Đan tay cùng xây đất lành,  
Việt Nam đó, tươi rạng ngời,  
Rạng ngời trên khắp nhân gian.

[Chorus 3]
Ôi, Việt Nam, ngọn gió reo muôn hướng,  
Dòng yêu thương nối đất nước dài thêm.  
Dẫu xa cách, vẫn gần qua tháng năm,  
Đất mẹ hiền, muôn đời vang tiếng ca.  

[Outro]
Việt Nam yêu dấu, nơi con tim hòa chung nhịp,  
Mãi vang lên lời hứa kiên trung, sắt son.  
Những mạch nguồn, những con đường luôn xanh,  
Việt Nam ơi, mãi mãi là tình yêu.